/**
 * 入力の dynamicSystem から period を推定し、対応するモック文を返す。
 * 末尾にデモ表示である旨を明示する。
 */
export function mockClaude(request: LLMRequest): string {
  const period = detectPeriod(request.dynamicSystem);
  const body = MOCK_BY_PERIOD[period] ?? MOCK_BY_PERIOD.monthly;

  return `${body}

──
※ これはデモ表示です。実際の AI 解釈は ANTHROPIC_API_KEY を設定すると有効になります。`;
}

function detectPeriod(dynamicSystem: string): string {
  if (dynamicSystem.includes("日運")) return "daily";
  if (dynamicSystem.includes("月運")) return "monthly";
  if (dynamicSystem.includes("年運") || dynamicSystem.includes("流年"))
    return "yearly";
  if (dynamicSystem.includes("大運")) return "decadal";
  return "monthly";
}
